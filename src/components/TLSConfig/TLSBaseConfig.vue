<template>
  <div class="TLS-base-config">
    <div class="TLS-base-config-title part-header">
      {{ $t('Base.tlsConfig') }}
    </div>
    <div>
      <el-checkbox
        v-if="showEnable"
        v-model="record.enable"
        :label="enableLabel || $t('Base.enableTLS')"
        border
      />
      <el-checkbox
        v-model="record.verify"
        :label="verifyLabel || $t('Base.tlsVerify')"
        :true-label="SSL_VERIFY_VALUE_MAP.get(true)"
        :false-label="SSL_VERIFY_VALUE_MAP.get(false)"
        border
      />
    </div>
  </div>
</template>

<script lang="ts">
import { defineComponent } from 'vue'

export default defineComponent({
  name: 'TLSBaseConfig',
})
</script>

<script setup lang="ts">
import { defineProps, defineEmits, computed, PropType, WritableComputedRef } from 'vue'
import { SSL_VERIFY_VALUE_MAP } from '@/common/constants'
import { SSL } from '@/types/common'
import useI18nTl from '@/hooks/useI18nTl'

const props = defineProps({
  modelValue: {
    type: Object as PropType<SSL>,
    default: () => ({}),
  },
  showEnable: {
    type: Boolean,
    default: true,
  },
  enableLabel: {
    type: String,
    default: function () {
      try {
        return useI18nTl('Base').tl('enableTLS')
      } catch (error) {
        console.error(error)
        return undefined
      }
    },
  },
  verifyLabel: {
    type: String,
    default: function () {
      try {
        return useI18nTl('Base').tl('tlsVerify')
      } catch (error) {
        console.error(error)
        return undefined
      }
    },
  },
})

const emit = defineEmits(['update:modelValue'])

const record: WritableComputedRef<SSL> = computed({
  get() {
    return props.modelValue
  },
  set(val) {
    emit('update:modelValue', val)
  },
})
</script>

<style lang="scss">
.TLS-base-config {
  margin-bottom: 20px;
  .TLS-base-config-title {
    margin-top: 0px;
    color: var(--color-title-primary);
    font-weight: bold;
    line-height: 20px;
  }
}
</style>
